def f1(a, b):
    """Compute the sum of its arguments.

    Parameters
    ----------
    a : int, float or complex
        The first number of the sum.
    b : int, float or complex
        The second number of the sum.

    Returns
    -------
    int, float or complex
        The sum of `a` and `b`

    """
    return a + b

def f2(a, b):
    """Compute the division of its arguments.

    Parameters
    ----------
    a : int, float or complex
        The numerator of the division.
    b : int, float or complex
        The denominator of the division. Possibly 0.

    Returns
    -------
    int, float or complex
        The division of `a` and `b`

    """
    return a / b